import 'package:flutter/material.dart';
import 'package:flutter_dotenv/flutter_dotenv.dart';
import 'package:flutter_riverpod/flutter_riverpod.dart';
import 'package:firebase_core/firebase_core.dart';
import 'package:firebase_messaging/firebase_messaging.dart';
import 'package:flutter_native_splash/flutter_native_splash.dart';

import 'constants/colors.dart';
import 'router.dart';
import 'services/api_service.dart';
import 'firebase_options.dart'; // generated by flutterfire configure

// ── Background FCM handler ─────────────────────────────
@pragma('vm:entry-point')
Future<void> _firebaseMessagingBackgroundHandler(RemoteMessage message) async {
  await Firebase.initializeApp(options: DefaultFirebaseOptions.currentPlatform);
  debugPrint('🔔 Background FCM: ${message.messageId}');
}

Future<void> main() async {
  // ✨ 1. Capture the binding so we can pass it to the splash screen
  WidgetsBinding widgetsBinding = WidgetsFlutterBinding.ensureInitialized();

  // ✨ 2. FREEZE THE NATIVE SPLASH SCREEN!
  FlutterNativeSplash.preserve(widgetsBinding: widgetsBinding);

  debugPrint("🚦 Loading .env...");
  try {
    await dotenv.load(fileName: '.env');
  } catch (e) {
    debugPrint("⚠️ .env issue: $e");
  }

  debugPrint("🔥 Initializing Firebase...");
  try {
    if (Firebase.apps.isEmpty) {
      await Firebase.initializeApp(
          options: DefaultFirebaseOptions.currentPlatform);
    }
    FirebaseMessaging.onBackgroundMessage(_firebaseMessagingBackgroundHandler);
  } catch (e) {
    debugPrint("⚠️ Firebase skipped: $e");
  }

  debugPrint("🌐 Initializing API...");
  try {
    ApiService.instance.init();
  } catch (e) {
    debugPrint("⚠️ API issue: $e");
  }

  debugPrint("✅ ALL SYSTEMS GO! Launching UI...");
  runApp(
    const ProviderScope(
      // 👈 THIS IS REQUIRED FOR THE THEMES TO WORK
      child: MitraApp(),
    ),
  );
}

class MitraApp extends ConsumerWidget {
  const MitraApp({super.key});

  @override
  Widget build(BuildContext context, WidgetRef ref) {
    final router = ref.watch(routerProvider);

    return MaterialApp.router(
      title: 'MITRA',
      debugShowCheckedModeBanner: false,
      theme: mitraTheme(),
      routerConfig: router,
      supportedLocales: const [
        Locale('en'),
        Locale('hi'),
        Locale('ta'),
        Locale('te'),
        Locale('kn'),
        Locale('bn'),
        Locale('mr'),
        Locale('gu'),
      ],
    );
  }
}
